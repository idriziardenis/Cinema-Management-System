        public List<Payment> RetrieveALL()
        {
            List<Payment> All = new List<Payment>();
            try
            {
                using (SqlConnection sqlConnection = new SqlConnection(Connection.ConnectionString))
                {
                    sqlConnection.Open();
                    using (SqlCommand sqlCommand = new SqlCommand("usp_Payment_RetrieveALL", sqlConnection))
                    {
                        sqlCommand.CommandType = CommandType.StoredProcedure;
                        using (SqlDataReader dr = sqlCommand.ExecuteReader())
                        {
                            if (dr.HasRows)
                            {
                                while (dr.Read())
                                {
                                    var obj = new Payment();
                                    obj.ID = int.Parse(dr["PaymentID"].ToString());
                                    obj.CashPaymentDetails = new DACashPaymentDetails().Retrieve(int.Parse(dr["CashPaymentDetails"].ToString()));
                                    obj.BaseAuditObject = new BaseAudit();
